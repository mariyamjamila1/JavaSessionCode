package Practice;

public class loops {

	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++) {
			System.out.println("Hello World");
		}
		
		System.out.println("-----------------------");
	
		int h=10;
		while (h>0) {
			System.out.println("Hello World");
			h--;
		

			
		}
	}

}
